package arithmetic.substraction;

public class Intsubstraction {
    public void ints()
    {
        int no1=10;
        int no2=20;
        int substraction;
        substraction = no2 - no1;

        System.out.println("Substraction = " + substraction);
    }
    public static void main(String[] args)
    {
        Intsubstraction is = new Intsubstraction();
        is.ints();
    }
}


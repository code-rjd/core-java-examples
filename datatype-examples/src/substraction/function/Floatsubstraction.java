package substraction.function;

public class Floatsubstraction {
    public void floats()
    {
        float no1=3.14f;
        float no2=4.52f;
        float sub;
        sub = no2 - no1;
        System.out.println("substraction = " + sub);
    }
    public static void main(String[] args)
    {
        Floatsubstraction fm = new Floatsubstraction();
        fm.floats();
    }
}

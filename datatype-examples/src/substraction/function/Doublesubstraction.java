package substraction.function;

public class Doublesubstraction {
    public void doubles()
    {
        double no1=2.144556;
        double no2=3.524215;
        double sub;
        sub = no1 - no2;

        System.out.println("sub = " + sub);
    }
    public static void main(String[] args)
    {
        Doublesubstraction ds = new Doublesubstraction();
        ds.doubles();
    }
}
